public class AvgLen
{
   public static double go( String a, String b )
	{
	   
	   double aLength = a.length();
	   double bLength = b.length();
	   double averageLength = 0;
	   
	   averageLength = (aLength+bLength)/2;

		return averageLength;
	}
}